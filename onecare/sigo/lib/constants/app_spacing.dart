/// App-wide spacing constants for consistent padding, margins, and gaps
class AppSpacing {
  AppSpacing._(); // Private constructor to prevent instantiation

  // Spacing values (in logical pixels)
  static const double xs = 4.0;
  static const double sm = 8.0;
  static const double md = 12.0;
  static const double lg = 16.0;
  static const double xl = 20.0;
  static const double xxl = 24.0;
  static const double xxxl = 32.0;
  static const double huge = 48.0;

  // Common padding shortcuts
  static const double cardPadding = lg;
  static const double screenPadding = xxl;
  static const double listItemSpacing = md;
  static const double sectionSpacing = xxl;

  // Border radius
  static const double radiusSm = 4.0;
  static const double radiusMd = 8.0;
  static const double radiusLg = 12.0;
  static const double radiusXl = 16.0;

  // Icon sizes
  static const double iconSm = 16.0;
  static const double iconMd = 20.0;
  static const double iconLg = 24.0;
  static const double iconXl = 32.0;
}
